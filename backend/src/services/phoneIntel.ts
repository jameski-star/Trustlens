const KNOWN_DISPOSABLE_PREFIXES = ['+447', '+1900', '+1906', '+234800', '+234700'];

export function getPhoneIntel(phone: string): PhoneIntel {
  const cleaned = phone.replace(/[\s\-()]/g, '');
  const normalized = cleaned.startsWith('+') ? cleaned : `+${cleaned.replace(/^00/, '')}`;

  if (!/^\+\d{7,15}$/.test(normalized)) {
    return { isVirtual: false, isMobile: false, validFormat: false, normalized };
  }

  let cc = '';
  for (const code of Object.keys(COUNTRY_CODES).sort((a, b) => b.length - a.length)) {
    if (normalized.startsWith(`+${code}`)) {
      cc = code;
      break;
    }
  }

  const country = cc ? COUNTRY_CODES[cc] : undefined;
  const countryCode = cc;

  let provider: string | undefined;

  const sortedProviders = Object.keys(PROVIDER_PREFIXES).sort((a, b) => b.length - a.length);
  for (const prefix of sortedProviders) {
    if (normalized.startsWith(prefix)) {
      provider = PROVIDER_PREFIXES[prefix];
      break;
    }
  }

  const isVirtual = VIRTUAL_NUMBER_PATTERNS.some(p => p.test(normalized));
  const isVoIP = VOIP_PREFIXES.some(p => normalized.startsWith(p));
  const isDisposable = KNOWN_DISPOSABLE_PREFIXES.some(p => normalized.startsWith(p));
  const isMobile = normalized.length >= 10 &&
    !isVirtual &&
    !isVoIP &&
    ![800, 808, 888, 900, 976].some(p => normalized.includes(String(p)));

  return {
    country: country ? { name: country.name, code: countryCode, region: country.region } : undefined,
    provider,
    isVirtual: isVirtual || isVoIP || isDisposable,
    isMobile,
    validFormat: true,
    normalized,
  };
}
